<h1>2. What is SvelteKit ?</h1>

<h2>At its core, SvelteKit's job boils down to three things :</h2>

<ul class="list-disc p-6 text-2xl">
	<li class="p-2">Routing — figure out which route matches an incoming request</li>
	<li class="p-2">Loading — get the data needed by the route</li>
	<li class="p-2">
		Rendering — generate some HTML (on the server) or update the DOM (in the browser)
	</li>
	<li class="p-2">...</li>
