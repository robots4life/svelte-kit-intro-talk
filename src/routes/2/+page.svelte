<div class="block mb-32">
	<h1>1. What is Svelte ?</h1>
	<h2>
		Svelte is a tool for building web applications. Like other user interface frameworks, it
		allows you to build your app declaratively out of components that combine markup, styles and
		behaviours. These components are compiled into small, efficient JavaScript modules that
		eliminate overhead traditionally associated with UI frameworks.
	</h2>
</div>
<div class="block mb-32">
	<h3>
		In Svelte, an application is composed from one or more components. A component is a reusable
		self-contained block of code that encapsulates HTML, CSS and JavaScript that belong
		together, written into a .svelte file.
	</h3>
